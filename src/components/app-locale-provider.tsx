const AppLocaleContext = createContext<AppLocaleContextValue | null>(null);

function resolveHtmlLang(locale: AppLocale): string {
  if (locale === "zh") return "zh-CN";
  if (locale === "ja") return "ja-JP";
  return "en";
}

function resolveInitialLocale(): AppLocale {
  if (typeof window === "undefined") return "en";
  const stored = window.localStorage.getItem(STORAGE_KEY);
  if (stored === "zh" || stored === "en" || stored === "ja") return stored;
  const nav = window.navigator.language.toLowerCase();
  if (nav.startsWith("zh")) return "zh";
  if (nav.startsWith("ja")) return "ja";
  return "en";
}

function interpolate(
  template: string,
  params: Record<string, string | number> = {},
): string {
  return Object.entries(params).reduce(
    (result, [key, value]) => result.replaceAll(`{${key}}`, String(value)),
    template,
  );
}

export function AppLocaleProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<AppLocale>("en");

  useEffect(() => {
    const initial = resolveInitialLocale();
    setLocaleState(initial);
    document.documentElement.lang = resolveHtmlLang(initial);
  }, []);

  const value = useMemo<AppLocaleContextValue>(
    () => ({
      locale,
      setLocale: (next) => {
        setLocaleState(next);
        if (typeof window !== "undefined") {
          window.localStorage.setItem(STORAGE_KEY, next);
          document.documentElement.lang = resolveHtmlLang(next);
        }
      },
      t: (key, params) =>
        interpolate(
          translations[locale][key] ?? translations.en[key] ?? key,
          params,
        ),
    }),
    [locale],
  );

  return (
    <AppLocaleContext.Provider value={value}>
      {children}
    </AppLocaleContext.Provider>
  );
}

export function useAppLocale() {
  const context = useContext(AppLocaleContext);
  if (!context) {
    throw new Error("useAppLocale must be used within AppLocaleProvider");
  }
  return context;
}
